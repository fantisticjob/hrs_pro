package com.hausontech.hrs.bean.dataProcess;

import com.hausontech.hrs.bean.AuditBean2;

public class LedgerBean extends AuditBean2 {
	private String lederCode;
	private String lederName;
	public LedgerBean(String code, String name) {
		this.lederCode = code;
		this.lederName = name;
	}
	
	public LedgerBean() {}
	
	public String getLederCode() {
		return lederCode;
	}
	public void setLederCode(String lederCode) {
		this.lederCode = lederCode;
	}
	public String getLederName() {
		return lederName;
	}
	public void setLederName(String lederName) {
		this.lederName = lederName;
	}
	
	

}

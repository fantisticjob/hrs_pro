package com.hausontech.hrs.bean.engine;

public class ItemCalculationBean {
	private long itemDefId;
	private long itemCalId;
	private String itemDefCode;
	private int seqNumber;
	private String operator;
	private String opItemDefCode;
	private String constant;
	private boolean isConstant = false;

	public long getItemCalId() {
		return itemCalId;
	}

	public void setItemCalId(long itemCalId) {
		this.itemCalId = itemCalId;
	}

	public long getItemDefId() {
		return itemDefId;
	}

	public void setItemDefId(long itemDefId) {
		this.itemDefId = itemDefId;
	}

	public String getItemDefCode() {
		return itemDefCode;
	}

	public void setItemDefCode(String itemDefCode) {
		this.itemDefCode = itemDefCode;
	}

	public int getSeqNumber() {
		return seqNumber;
	}

	public void setSeqNumber(int seqNumber) {
		this.seqNumber = seqNumber;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

	public String getOpItemDefCode() {
		return opItemDefCode;
	}

	public void setOpItemDefCode(String opItemDefCode) {
		this.opItemDefCode = opItemDefCode;
	}

	public String getConstant() {
		return constant;
	}

	public void setConstant(String constant) {
		this.constant = constant;
	}

	public boolean isConstant() {
		return isConstant;
	}

	public void setConstant(boolean isConstant) {
		this.isConstant = isConstant;
	}

}

package com.hausontech.hrs.bean.dataProcess;

import com.hausontech.hrs.bean.AuditBean;

public class PeriodBean extends AuditBean {	
	private String periodName;
	private int periodYear;
	private int periodNum;
	private int quarterNum;
	private String adjFlag;
	
	public PeriodBean(String periodName) {
		this.periodName = periodName;
	}
	
	public PeriodBean(){}
	
	public String getPeriodName() {
		return periodName;
	}
	public void setPeriodName(String periodName) {
		this.periodName = periodName;
	}
	public int getPeriodYear() {
		return periodYear;
	}
	public void setPeriodYear(int periodYear) {
		this.periodYear = periodYear;
	}
	public int getPeriodNum() {
		return periodNum;
	}
	public void setPeriodNum(int periodNum) {
		this.periodNum = periodNum;
	}
	public int getQuarterNum() {
		return quarterNum;
	}
	public void setQuarterNum(int quarterNum) {
		this.quarterNum = quarterNum;
	}
	public String getAdjFlag() {
		return adjFlag;
	}
	public void setAdjFlag(String adjFlag) {
		this.adjFlag = adjFlag;
	}
}
